class Game {
  final int id;
  final String name;
  final String type;
  final String genre;
  final double duration;
  final String platform;
  final double price;
  final String releasedDate;
  final double ageRating;
  final double size;
  final String description;
  final String image;
  final String company;

  Game({
    required this.id,
    required this.name,
    required this.type,
    required this.genre,
    required this.duration,
    required this.platform,
    required this.price,
    required this.releasedDate,
    required this.ageRating,
    required this.size,
    required this.description,
    required this.image,
    required this.company,
  });
}
